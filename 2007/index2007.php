				</div>
				<p class="top">The International Workshop on Protocols for Fast Long-Distance
  Networks at CERN (<a href="http://datatag.web.cern.ch/datatag/pfldnet2003/" target="_blank">PFLDnet2003</a>),
  Argonne (<a href="http://www-didc.lbl.gov/PFLDnet2004/" target="_blank">PFLDnet2004</a>),
  Lyon (<a href="http://www.ens-lyon.fr/LIP/RESO/pfldnet2005/" target="_blank">PFLDnet2005</a>)
  and Nara (<a href="http://www.hpcc.jp/pfldnet2006/" target="_blank">PFLDnet2006</a>)
  were very successful in bringing together many researchers from all over the
  world including North America, Europe and Asia who are working on these problems.
  This workshop will continue this tradition, and provide a perfect forum for
  researchers in this area to exchange ideas and experience.</p>
				<h2>NEWS:</h2>
<p>For the first time,  PFLDnet will host a TUTORIAL, on the morning of Friday February&nbsp;9th, 2007 . This tutorial will be given by <a href="http://ee2.caltech.edu/People/Faculty/low.html" target="_blank">Prof. Steven Low</a>, with a tentative title of &quot;Flow control theory for practitioners&quot;. </p>
