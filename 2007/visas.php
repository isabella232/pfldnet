  <p>*Delegates' relevant details for a "letter of invitation" include:<br>
- the traveler's name on the passport<br>
- the traveler's date of birth, <br>
- country passport was issued from, <br>
- the passport number</P>
  <P>All <a href="http://travel.state.gov/visa/temp/temp_1305.html">international visitors</a> entering the USA must possess valid passports.
<P>Citizens of some countries may be required to apply for <a href="http://travel.state.gov/visa/">entry visas</a> in advance.<br>
PFLDnet2007 can supply a letter of invitation by request, to support a visa application made by a registered delegate (see above for details.)</P>
Depending on the country, visa applications can take <strong>1 or 2 months</strong>  to be processed. It is therefore recommended that participants contact their <a href="http://usembassy.state.gov/">local US Consular Offices</a> to determine if a visa is required and how long the visa takes to process, as<strong> soon</strong> as possible.
<P>  USA has a <a href="http://www.travel.state.gov/visa/temp/without/without_1990.html#2">visa  waiver program</a> for some countries, under certain circumstances.</P>
