<h4>Topics:</h4>

PFLDNeT 2010 covers all aspects related to transport protocols for the
current and future Internet, including, but not limited to:<br>

- Transport protocol development<br>
- Enhancements to TCP and other transports<br>
- Innovative congestion control mechanisms<br>
- Novel data transport protocols designed for new networks and applications<br>
- Transport services for data center networks and grids<br>
- Transport services for wireless and sensor networks<br>
- Explicit signaling protocols: optimization criteria and deployment strategies<br>
- Pacing and shaping of traffic<br>
- Parallel transfers and multi-streaming<br>
- Performance evaluation<br>
- Modeling and simulation-based results<br>
- Interaction of transport protocols and network equipment<br>
- Experiments on real networks and live measurements<br>
- Transport protocol benchmarking<br>
- Transport over optical networks<br>
- Transport implementation and hardware issues<br>
- End system performance<br>
- Data replication and striping<br>
- Applications with demanding or unusual network performance requirements<br>
- Bulk-data transfer applications<br>
- Quality-of-service and scalability issues<br>
- Multicast<br>
