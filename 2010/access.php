<p>
	 <H2>From Philadelphia Airport:</H2>
	 <a href="http://www.phl.org">Philadelphia International Airport</a>
	 is a 1.5-hour drive from the venue,
	 and may be preferred for those planning to continue on
	 to <a href="http://conferences.sigcomm.org/co-next/2010/">CoNEXT</a>.<br>
	 Public transport is also quite convenient and can be used to
	 get from the Philadelphia Airport to Lancaster as follows:<br>
	 <b>1.</b> Use the 
	 <a href="http://septa.org/schedules/rail/index.html">SEPTA Airport Line</a>
	 to get from the airport to Philadelphia 30th Street Station.
	 Tickets can be purchased 
	 at the train station. (Travel time: ~30 minutes)<br>
	 <b>2.</b> Use <a href="http://www.amtrak.com">Amtrak</a>
	 to get from "30th Street" Station (station code: PHL) 
	 to Lancaster (station code: LNC). Tickets can be purchased at 
	 kiosks or windows 
	 at 30th Street Station, or <a href="http://www.amtrak.com">online</a>. 
	 If you purchase your tickets online, and if you miss your train, go
	 to the customer service desk at 30th Street Station and pay the 
     difference (if any) for the next train.  In case the customer service 
     desk is closed, assume that the ticket will work on the following train.
	 (Travel time: ~70 minutes)<br>
	 <b>3.</b>The Mariott is a ~25-minute walk 
		 (<a href="http://maps.google.com/maps?f=d&source=embed&saddr=Lancaster+Amtrak,+Lancaster,+PA&daddr=Lancaster+Marriott+at+Penn+Square,+Lancaster,+PA&geocode=FaUtYwId_qJz-ynbbjkahSTGiTEV_D_wL2Wj4g%3BFcXqYgIdsKtz-yEdOStVjs5O_infEI6P7STGiTG4n8MR0H61Fw&hl=en&mra=ltm&dirflg=w&sll=40.045725,-76.3081&sspn=0.023096,0.051627&ie=UTF8&ll=40.045752,-76.307988&spn=0.023096,0.051627&z=15">walking directions</a>)
		 or a 5-minute taxicab ride
		 from the Lancaster Amtrak train station.<br>
</p>
