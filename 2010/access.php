<TD width="778"><H1><IMG src="images/cap_sub.gif" alt=" " height="36" width="20" align="absmiddle" border="0" hspace="5" vspace="5">Getting There</H1>
<div class="top">
<p>
	 <H2> General Instructions: </H2>
	 General instructions on getting to the venue are listed 
	 <a href="http://www.fandm.edu/get-here">here</a>.  
	 The closest airport is Harrisburg International Airport (HIA),
	 at a ~40-minute drive from the venue.
	 Harrisburg is a small airport, however,
	 and you are likely to have more options flying into 
	 Philadelphia International Airport (PHL).
	 (particularly for those planning to attend CoNEXT in Philadelphia.)
	 Note that PHL is not mentioned in the page linked to above;
	 more detailed information on getting to the venue from 
     HIA and PHL follows.
</p>
     <H2>From Harrisburg Airport:</H2>
<p>
	 <a href="">Harrisburg International Airport</a> is the nearest airport
	 to the venue, 
	 and is a ~40-minute drive from the venue.
	 We have made arrangements with the following shuttle company:<br><br>
	 Unique Limousine of Harrisburg:<br>
	 Phone number: +1-800-298-8053<br>
	 Town Car (upto 3 people): USD 116 (includes gratuity)<br>
	 Van (4-10 people): USD 154 (includes gratuity)<br>
	 Cancellation:  Allowed upto ~3 hours before pick-up time.<br><br>

	 While you should budget for the worst case cost of USD 116 per person,
	 we have arranged for sharing, and the best case cost is USD 15.4
     when 10 people share a van. The shuttle company is co-ordinating 
	 grouping and scheduling, and will try to accomodate sharing when possible.
     When you call to make your reservation,
	 please let them know the following: <br>
	 (i) that you are with <b>the PFLDNeT group</b><br>
	 (ii) that you are willing to share, and<br>
	 (iii) how long you are willing to wait to share a shuttle.<br>

	 You can call the phone number above closer to the travel date
	 and check whether (and how much) you will be sharing.
</p> 

