<div class="top">
	 <H2>Accomodation:</H2>
<p>
A block has been reserved for PFLDNeT attendees at 
	 The Lancaster Mariott in downtown Lancaster <b>(until November 15.)</b><br>
Please use <a href="http://www.marriott.com/hotels/travel/lnsmc?groupCode=frmfrma&app=resvlink&fromDate=11/27/10&toDate=11/29/10">this reservation link</a>
to make reservations in the PFLDNeT block.

<p>The Marriott is providing a group price of $139 per night, which includes wireless access.</p>

<p>The <a href="http://www.marriott.com/hotels/travel/lnsmc-lancaster-marriott-at-penn-square/">Lancaster Mariott at Penn Square</a>
is in downtown Lancaster (a National Historic District)
which is a pleasant 25-minute walk (~1 mile/1.6 km) through town
	from the workshop venue (F&M).
Transportation will also be provided from the hotel
in the morning, and to the hotel in the afternoon/evening.</p>
</p>

<p>
	Other hotels in the area are listed <a href="http://fandm.edu/visit/dining-staying-in-lancaster#lodging">here</a>,
	with the Lancaster Arts Hotel being the closest.
</p>

